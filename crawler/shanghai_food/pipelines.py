# Scrapy Pipeline
# 功能：数据清洗和入库
# 包含：
# - DataCleanPipeline       数据清洗
#   - 去重：基于餐厅名称+地址+电话组合去重
#   - 格式化：统一日期格式、价格格式、评分格式
#   - 异常值处理：价格为0、评分超出范围等
#   - 缺失值处理：标记或填充缺失字段
#   - 数据脱敏：手机号、用户ID脱敏
#   - 数据验证：完整性和合法性校验
#
# - MySQLPipeline           结构化数据写入MySQL（餐厅信息、评论、菜品）
# - DataQualityPipeline     数据质量监控（记录清洗统计、异常数据进入待修复队列）

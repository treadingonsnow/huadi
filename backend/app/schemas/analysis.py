# 数据分析相关 Pydantic Schema
# 包含：
# - AreaAnalysisResponse       区域分析结果（商圈密度、菜系分布、消费水平、热力图数据）
# - PriceAnalysisResponse      价格分析结果（价格区间分布、菜系价格对比、趋势数据、性价比排行）
# - ReviewAnalysisResponse     口碑分析结果（评分分布、情感分析、热门菜品、词云数据）
# - TrendAnalysisResponse      趋势分析结果（菜系趋势、新店趋势、消费预测、季节性数据）
# - CompetitorAnalysisResponse 竞品分析结果（对比数据、优劣势、市场份额、SWOT）
